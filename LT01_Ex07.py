# Objetivo: Receba os valores do comprimento, largura e altura de um paralelepípedo. Calcule e mostre seu volume.
# Programador: Gabriel Ribeiro
# Data de Elaboração: 22/10/2024

comprimento = float(input("Valor do comprimento do paralelepípedo: "))
largura = float(input("Valor da largura do paralelepípedo: "))
altura = float(input("Valor da altura do paralelepípedo: "))

volume = (comprimento * largura * altura)

print("Valor do volume do paralelepípedo:" , volume)
