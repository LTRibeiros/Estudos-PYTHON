# Objetivo: Receber o valor da base e o valor da altura de um triângulo. Calcular e mostrar sua área.
# Programador: Gabriel Ribeiro
# Data de Elaboração: 22/10/2024

base = float(input("Valor da base: "))
altura = float(input("Valor da altura: "))

area = (base * altura) / 2

print("Valor da área do triângulo:" , area)
