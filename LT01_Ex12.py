# Objetivo: Receba o ano de nascimento e o ano atual. Calcule e mostre a sua idade e quantos anos terá daqui a 17 anos.
# Programador: Gabriel Ribeiro
# Data de Elaboração: 22/10/2024

A_nas = int(input("Ano de nascimento: "))
A_atual = 2024

idade = (A_atual - A_nas)
print("Idade atual:" , idade)

idade_F = idade + 17
print("Idade daqui 17 anos:" , idade_F)
